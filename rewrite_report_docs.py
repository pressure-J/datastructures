def wrap_text(draw, text, max_width, fnt):
    lines = []
    for raw in text.split("\n"):
        line = ""
        for ch in raw:
            test = line + ch
            if draw.textbbox((0, 0), test, font=fnt)[2] <= max_width:
                line = test
            else:
                if line:
                    lines.append(line)
                line = ch
        if line:
            lines.append(line)
    return lines


def centered_text(draw, box, text, fnt=FONT_BODY, fill=None):
    fill = fill or PALETTE["ink"]
    x1, y1, x2, y2 = box
    max_width = x2 - x1 - 28
    lines = wrap_text(draw, text, max_width, fnt)
    heights = [draw.textbbox((0, 0), line, font=fnt)[3] for line in lines]
    total = sum(heights) + max(0, len(lines) - 1) * 6
    y = y1 + (y2 - y1 - total) / 2
    for line, h in zip(lines, heights):
        w = draw.textbbox((0, 0), line, font=fnt)[2]
        draw.text((x1 + (x2 - x1 - w) / 2, y), line, font=fnt, fill=fill)
        y += h + 6


def rect(draw, box, text, fill, outline=None, fnt=FONT_BODY):
    outline = outline or PALETTE["line"]
    draw.rounded_rectangle(box, radius=12, fill=fill, outline=outline, width=2)
    centered_text(draw, box, text, fnt=fnt)


def arrow(draw, start, end, color=None, width=3):
    color = color or PALETTE["line"]
    draw.line([start, end], fill=color, width=width)
    x1, y1 = start
    x2, y2 = end
    if abs(x2 - x1) >= abs(y2 - y1):
        sign = 1 if x2 > x1 else -1
        pts = [(x2, y2), (x2 - sign * 14, y2 - 8), (x2 - sign * 14, y2 + 8)]
    else:
        sign = 1 if y2 > y1 else -1
        pts = [(x2, y2), (x2 - 8, y2 - sign * 14), (x2 + 8, y2 - sign * 14)]
    draw.polygon(pts, fill=color)


